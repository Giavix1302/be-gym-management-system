import { StatusCodes } from 'http-status-codes'
import { progressService } from '../service/progress.service'

const createNew = async (req, res, next) => {
  try {
    const result = await progressService.createNew(req)

    if (result.success) {
      res.status(StatusCodes.OK).json(result)
    } else {
      res.status(StatusCodes.BAD_REQUEST).json(result)
    }
  } catch (error) {
    next(error)
  }
}

const getDetail = async (req, res, next) => {
  try {
    const userId = req.params.id
    const user = await progressService.getDetail(userId)
    res.status(StatusCodes.OK).json(user)
  } catch (error) {
    next(error)
  }
}

const updateInfo = async (req, res, next) => {
  try {
    const locationId = req.params.id
    const result = await progressService.updateInfo(locationId, req.body)
    if (result.success) {
      res.status(StatusCodes.OK).json(result)
    } else {
      res.status(StatusCodes.NOT_FOUND).json(result)
    }
  } catch (error) {
    next(error)
  }
}

const deleteLocation = async (req, res, next) => {
  try {
    const userId = req.params.id
    const result = await progressService.deleteLocation(userId)
    if (result.success) {
      res.status(StatusCodes.OK).json(result)
    } else {
      res.status(StatusCodes.NOT_FOUND).json(result)
    }
  } catch (error) {
    next(error)
  }
}

export const progressController = {
  createNew,
  getDetail,
  updateInfo,
  deleteLocation,
}
